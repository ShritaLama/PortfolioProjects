--Looking at Total Cases vs Total Deaths

Select Location, date, total_cases, total_deaths, (total_deaths/total_cases)*100 as DeathPercentage
From PortfolioProject..CovidDeaths
Where Location like '%nepal%'
order by 1,2

--Looking at Total Cases vs Population 

Select Location, date, total_cases, Population, (total_deaths/population)*100 as PercetPopulationInfected
From PortfolioProject..CovidDeaths
Where Location like '%nepal%'
order by 1,2


--Looking at Countries with Highest Infextion Rate compared to Population

Select Location, Population, MAX(total_cases) as HighestInfectionCount, MAX (total_cases/population)*100 as PercetPopulationInfected
From PortfolioProject..CovidDeaths
Group by Location, Population
order by PercetPopulationInfected desc


--Showing the Countries with Highest Death Count per Population

Select Location, MAX(cast (Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
Where continent is not null
Group by Location
order by TotalDeathCount desc


--Showing the continents with the highest death count per population

Select continent, MAX(cast (Total_deaths as int)) as TotalDeathCount
From PortfolioProject..CovidDeaths
Where continent is not null
Group by continent
order by TotalDeathCount desc

